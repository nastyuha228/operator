a = int(input('Введите коэффициент a: '))
b = int(input('Введите коэффициент b: '))
c = int(input('Введите коэффициент c: '))

D = b ** 2 - 4 * a * c

if D == 0:
    x = (-b / (2 * a))
    print(f'Корень уравнения - {x}')
elif D > 0:
    x1 = ((-b + (D ** 0.5)) / (2 * a))
    x2 = ((-b - (D ** 0.5)) / (2 * a))
    print(f'Корни уравнения - ({x1}, {x2})')
else:
    print('Корней нет')